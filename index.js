// 4. indexof () Method 
let num = ["one", "two", "three", "four"];

console.log(num.indexOf("one"));
console.log(num.indexOf("two"));
console.log(num.indexOf("three"));
console.log(num.indexOf("five"));

console.log(["one","two", "three", "four"].indexOf("one"));

